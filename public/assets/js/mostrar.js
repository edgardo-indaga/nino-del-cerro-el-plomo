function mostrarMuseo() {
    if (window.innerWidth > 768){
        var frontmuseo = document.getElementById('frontmuseo');
        var museo = document.getElementById('museo');

        frontmuseo.style.display = 'none';
        museo.style.display = 'flex';
    }

}
